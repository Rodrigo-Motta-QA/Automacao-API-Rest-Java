package Pojo;

public class ComponentePojo {
    private String componenteNome;
    private int omponenteQuantidade;

    public String getComponenteNome() {
        return componenteNome;
    }

    public void setComponenteNome(String componenteNome) {
        this.componenteNome = componenteNome;
    }

    public int getOmponenteQuantidade() {
        return omponenteQuantidade;
    }

    public void setOmponenteQuantidade(int omponenteQuantidade) {
        this.omponenteQuantidade = omponenteQuantidade;
    }
}
